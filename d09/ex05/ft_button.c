int		ft_button(int i, int j, int k)
{
	if ((j <= i && i <= k) || (k <= i && i <= j))
		return (i);
	if ((i <= j && j <= k) || (k <= j && j <= i))
		return (j);
	if ((i <= k && k <= j) || (i <= k && k <= j))
		return (k);
	else
		return (0);
}
