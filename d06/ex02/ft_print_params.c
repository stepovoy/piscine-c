void	ft_putchar(char c);

int		main(int argc, char const *argv[])
{
	int i;
	int j;

	i = 1;
	while (i < argc && argc > 1)
	{
		j = 0;
		while (argv[i][j])
		{
			ft_putchar(argv[i][j]);
			j++;
		}
		i++;
		ft_putchar('\n');
	}
	return (0);
}
